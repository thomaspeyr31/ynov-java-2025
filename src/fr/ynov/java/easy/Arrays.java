package fr.ynov.java.easy;

public class Arrays {
    public static void main(String[] args) {
        int[] tableau = {10, 20, 30, 40, 50};

        System.out.println("affiche les nombres du tableau : ");
        for(int j=0;j<tableau.length;j++){
            System.out.print(tableau[j]);
        }
    }
}
